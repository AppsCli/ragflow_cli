def _load_extra_translations():
    path = os.path.join(os.path.dirname(__file__), "translations_extra.json")
    if os.path.exists(path):
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f)
    return {}


def main():
    with open(EN_ARB, "r", encoding="utf-8") as f:
        en = json.load(f)

    extra = _load_extra_translations()
    all_trans = {**TRANSLATIONS, **extra}

    # Collect string keys (exclude @@locale and @*)
    string_keys = [k for k in en if not k.startswith("@") and k != "@@locale"]

    for locale, trans in all_trans.items():
        out = {"@@locale": locale}
        for k in en:
            if k == "@@locale":
                continue
            if k.startswith("@"):
                out[k] = en[k]
                continue
            if k in trans:
                out[k] = trans[k]
            else:
                out[k] = en[k]
        path = os.path.join(ARB_DIR, f"app_{locale}.arb")
        with open(path, "w", encoding="utf-8") as f:
            json.dump(out, f, ensure_ascii=False, indent=2)
        print(f"Wrote {path}")

    # Check coverage
    for locale, trans in all_trans.items():
        missing = [k for k in string_keys if k not in trans]
        if missing:
            print(f"{locale}: missing {len(missing)} keys: {missing[:5]}...")


if __name__ == "__main__":
    main()
